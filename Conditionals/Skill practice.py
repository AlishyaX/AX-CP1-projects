user_grade = float(input('What is your grade percentage?'))


if user_grade >= 90:
    print('You have an A')
elif user_grade < 90 and user_grade >= 80:
    print('You have a B')
elif user_grade < 80 and user_grade >= 70:
    print('You have a C')
elif user_grade < 70 and user_grade >= 60:
    print('You have a D')
else:
    print('You have an F')


